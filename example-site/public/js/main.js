(() => {
  // <stdin>
  console.log("This site was generated by Hugo.");
  console.log("Theme: Lightning by Thundere75.");
  (function() {
    if (typeof document === "undefined") return;
    document.addEventListener("DOMContentLoaded", function() {
      const lightbox = document.getElementById("image-lightbox");
      if (!lightbox) return;
      const imgEl = lightbox.querySelector(".image-lightbox__img");
      const captionEl = lightbox.querySelector(".image-lightbox__caption");
      const closeBtn = lightbox.querySelector(".image-lightbox__close");
      const backdrop = lightbox.querySelector(".image-lightbox__backdrop");
      if (!imgEl || !captionEl || !closeBtn || !backdrop) return;
      let lastFocused = null;
      function openLightbox(src, alt, caption) {
        if (!src) return;
        lastFocused = document.activeElement;
        imgEl.src = src;
        imgEl.alt = alt || "";
        captionEl.textContent = caption || alt || "";
        lightbox.classList.add("is-active");
        lightbox.setAttribute("aria-hidden", "false");
        closeBtn.focus();
      }
      function closeLightbox() {
        lightbox.classList.remove("is-active");
        lightbox.setAttribute("aria-hidden", "true");
        imgEl.src = "";
        if (lastFocused && typeof lastFocused.focus === "function") {
          lastFocused.focus();
        }
      }
      document.addEventListener("click", function(event) {
        const img = event.target.closest("img");
        if (!img) return;
        const figure = img.closest("figure");
        if (!figure) return;
        event.preventDefault();
        const src = img.getAttribute("src");
        const alt = img.getAttribute("alt") || "";
        const dataCaption = img.getAttribute("data-caption") || "";
        const figcaption = figure.querySelector("figcaption");
        const figCaptionText = figcaption ? figcaption.textContent.trim() : "";
        const caption = dataCaption || figCaptionText || alt;
        openLightbox(src, alt, caption);
      });
      closeBtn.addEventListener("click", function() {
        closeLightbox();
      });
      backdrop.addEventListener("click", function() {
        closeLightbox();
      });
      document.addEventListener("keydown", function(event) {
        if (event.key === "Escape" && lightbox.classList.contains("is-active")) {
          closeLightbox();
        }
      });
    });
  })();
})();
